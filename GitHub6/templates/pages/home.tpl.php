<h3>Get to know us</h3>

<p>The idea of the The Pet Planet webshop was born in my head in 2006: I was thinking about an independent, Hungarian-owned "garage company". As a practical animal keeper and breeder, -with an IT background- it did not seem like an easy task, but it was not an impossible undertaking either. I knew what I wanted. This is the way to gain the trust of Internet hobby pet owners.</p>

<p>I knew it would be a long job, so 2006 was spent on the software design of the store. Then in 2007, software development began, as well as the photography (almost 85% of the photos in The Pet Planet webshop were taken in our own product photo studio), retouching, and the creation of accurate and informative product data sheets, which were essential for product presentations.</p>

<p>Since we opened in January 2008, our priority has been customer satisfaction, so we don't make promises we can't keep and we don't explain ourselves. To your complete satisfaction, we usually ship your order within 48 hours of placing it, although we promise 3 working days. This is due to the fact that we have limited stock and are dependent on our suppliers for some products.</p>

<img src="./images/Home/Cat1.jpg">

<p>The team at The Pet Planet also strives to provide a professional and well-supported answer to all pet owners' questions. So, in addition to basic questions about dogs and cats, we can also help answer your questions about marine molluscs and other rodents, ferrets, parrots and other birds, terraristics and freshwater and marine aquaristics. And if you have a pond in your garden, you can also contact us, because we have a serious expert in this field too!
Of course, we can't always answer your questions immediately, but thanks to our excellent team of experts, we can solve 99% of your problems in a short time! You can trust us: with your animals' needs in mind, we will take care of your comfort!</p>

